// ─── Public API ──────────────────────────────────────────────────────────────

/** All make names, sorted alphabetically */
export const getMakes = (): string[] =>
  VEHICLE_DATABASE.map((m) => m.name).sort();

/** All model names for a given make, sorted alphabetically */
export const getModels = (make: string): string[] => {
  const entry = VEHICLE_DATABASE.find(
    (m) => m.name.toLowerCase() === make.toLowerCase()
  );
  return entry ? entry.models.map((m) => m.name).sort() : [];
};

/** Year range for a specific make + model (descending, newest first) */
export const getYears = (make: string, model: string): number[] => {
  const makeEntry = VEHICLE_DATABASE.find(
    (m) => m.name.toLowerCase() === make.toLowerCase()
  );
  if (!makeEntry) return [];
  const modelEntry = makeEntry.models.find(
    (m) => m.name.toLowerCase() === model.toLowerCase()
  );
  if (!modelEntry) return [];
  const years: number[] = [];
  for (let y = modelEntry.yearTo; y >= modelEntry.yearFrom; y--) {
    years.push(y);
  }
  return years;
};

/** Generic year list (newest first) for when no model is selected yet */
export const getAllYears = (): number[] => {
  const current = new Date().getFullYear();
  return Array.from({ length: current - 1990 + 1 }, (_, i) => current - i);
};

